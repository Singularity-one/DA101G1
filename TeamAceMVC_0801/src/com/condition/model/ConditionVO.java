package com.condition.model;

public class ConditionVO {
	private String condition_no;
	private String itinerary_no;
	private String landscape_no;
	private String condition_content;
	private Integer condition_day;
	
	public String getCondition_no() {
		return condition_no;
	}
	public void setCondition_no(String condition_no) {
		this.condition_no = condition_no;
	}
	public String getItinerary_no() {
		return itinerary_no;
	}
	public void setItinerary_no(String itinerary_no) {
		this.itinerary_no = itinerary_no;
	}
	public String getLandscape_no() {
		return landscape_no;
	}
	public void setLandscape_no(String landscape_no) {
		this.landscape_no = landscape_no;
	}
	public String getCondition_content() {
		return condition_content;
	}
	public void setCondition_content(String condition_content) {
		this.condition_content = condition_content;
	}
	public Integer getCondition_day() {
		return condition_day;
	}
	public void setCondition_day(Integer condition_day) {
		this.condition_day = condition_day;
	}
	
	
	
	
	
	
}
