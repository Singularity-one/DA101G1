package com.gameroom.model;

public class GameroomVO {
	private String gameroom_no;
	private String room_psw;
	private String psw_status;
	private String blank_status;
	
	public String getGameroom_no() {
		return gameroom_no;
	}
	public void setGameroom_no(String gameroom_no) {
		this.gameroom_no = gameroom_no;
	}
	public String getRoom_psw() {
		return room_psw;
	}
	public void setRoom_psw(String room_psw) {
		this.room_psw = room_psw;
	}
	public String getPsw_status() {
		return psw_status;
	}
	public void setPsw_status(String psw_status) {
		this.psw_status = psw_status;
	}
	public String getBlank_status() {
		return blank_status;
	}
	public void setBlank_status(String blank_status) {
		this.blank_status = blank_status;
	}

	
	
	
}
