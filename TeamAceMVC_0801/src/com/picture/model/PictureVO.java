package com.picture.model;



public class PictureVO {
	private String pic_no;
	private String itinerary_no;
	private String mem_no;
	private String landscape_no;
	private byte [] pic;
	
	
	public String getPic_no() {
		return pic_no;
	}
	public void setPic_no(String pic_no) {
		this.pic_no = pic_no;
	}
	public String getItinerary_no() {
		return itinerary_no;
	}
	public void setItinerary_no(String itinerary_no) {
		this.itinerary_no = itinerary_no;
	}
	public String getMem_no() {
		return mem_no;
	}
	public void setMem_no(String mem_no) {
		this.mem_no = mem_no;
	}
	public String getLandscape_no() {
		return landscape_no;
	}
	public void setLandscape_no(String landscape_no) {
		this.landscape_no = landscape_no;
	}
	public byte[] getPic() {
		return pic;
	}
	public void setPic(byte[] pic) {
		this.pic = pic;
	}
	
	
	 
	

	
	
	
}
