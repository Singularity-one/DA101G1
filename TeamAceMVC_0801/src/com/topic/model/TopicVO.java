package com.topic.model;

public class TopicVO {
	private String topic_no;
	private String topicKind_no;
	private String topic_name;
	
	public String getTopic_no() {
		return topic_no;
	}
	public void setTopic_no(String topic_no) {
		this.topic_no = topic_no;
	}
	public String getTopicKind_no() {
		return topicKind_no;
	}
	public void setTopicKind_no(String topicKind_no) {
		this.topicKind_no = topicKind_no;
	}
	public String getTopic_name() {
		return topic_name;
	}
	public void setTopic_name(String topic_name) {
		this.topic_name = topic_name;
	}

	
}
